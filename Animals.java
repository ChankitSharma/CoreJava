/**
 * Program to demonstrate single inheritance. Animals is Parent class of that Programme.
 * 
 * @author Chankit
 */
public class Animals {

	/**
	 * This is the eat method
	 */
	void eat() {
		System.out.println("Eating...");
	}

	/**
	 * This is the run method
	 */
	void run() {
		System.out.println("Running...");
	}

}