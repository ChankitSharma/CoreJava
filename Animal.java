/**
 * Program to demonstrate of Hierarchical_Inheritance
 * 
 * @author Chankit
 */
public class Animal {

	/**
	 * This is the eat method for the animal
	 */
	void eat() {
		System.out.println("Eating...");
	}

	/**
	 * This is the run method for the animal
	 */
	void run() {
		System.out.println("Running...");
	}

}