/**
 * Program to demonstrate comparable and comparator interface, This Class is
 * used to implements comparable
 * 
 * @author Chankit
 */
public class Person {

	private String name;
	private int age;

	public Person(String name, int age) {
		this.name = name;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

}